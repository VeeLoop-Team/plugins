public class VideoPlayerPlugin{

  public static void registerWith(Registrar registrar) {
    final MethodChannel channel =
        new MethodChannel(registrar.messenger(), "flutter.io/videoPlayer");

    MethodChannelHandler methodChannelHandler = new MethodChannelHandler();
    channel.setMethodCallHandler(methodChannelHandler);
    registrar.addViewDestroyListener(
        new PluginRegistry.ViewDestroyListener() {
          @Override
          public boolean onViewDestroy(FlutterNativeView view) {
            methodChannelHandler.onDestroy();
            return false; // We are not interested in assuming ownership of the NativeView.
          }
        });
  }
}
